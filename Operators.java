public class Operators{
    public static void main(String[] args){
        int a = 10;
        int b = 20;

        // Arithmetic Operators : +, -, *, /, %
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        // Assignment Operators : =, +=, -=, *=, /= 
        int c = 5;
        c += a; //15 output
        System.out.println(c);
        
    }
}
